package it.eng.spagobi.tools.dataset.notifier;

import it.eng.spagobi.utilities.Helper;

public class UserLabelId {

	private String user;
	private String label;
	
	public UserLabelId(String user, String label) {
		Helper.checkNotNullNotTrimNotEmpty(user, "user");
		Helper.checkNotNullNotTrimNotEmpty(label, "label");
		
		this.user = user;
		this.label = label;
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((label == null) ? 0 : label.hashCode());
		result = prime * result + ((user == null) ? 0 : user.hashCode());
		return result;
	}
	
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		UserLabelId other = (UserLabelId) obj;
		if (label == null) {
			if (other.label != null)
				return false;
		} else if (!label.equals(other.label))
			return false;
		if (user == null) {
			if (other.user != null)
				return false;
		} else if (!user.equals(other.user))
			return false;
		return true;
	}
	
	
}
